    private static String getProducts() {
        ws.ProductoController_Service service = new ws.ProductoController_Service();
        ws.ProductoController port = service.getProductoControllerPort();
        return port.getProducts();
    }

    private static String sellProduct(int code, int cantidad, double saldo) {
        ws.ProductoController_Service service = new ws.ProductoController_Service();
        ws.ProductoController port = service.getProductoControllerPort();
        return port.sellProduct(code, cantidad, saldo);
    }

    private static String getProduct(int code) {
        ws.ProductoController_Service service = new ws.ProductoController_Service();
        ws.ProductoController port = service.getProductoControllerPort();
        return port.getProduct(code);
    }

    private static String getPersona(int code) {
        ws.usuario.PersonaController_Service service = new ws.usuario.PersonaController_Service();
        ws.usuario.PersonaController port = service.getPersonaControllerPort();
        return port.getPersona(code);
    }

    private static double getSaldo(int code) {
        ws.usuario.PersonaController_Service service = new ws.usuario.PersonaController_Service();
        ws.usuario.PersonaController port = service.getPersonaControllerPort();
        return port.getSaldo(code);
    }

    private static String updateSaldo(int code, double nuevoSaldo) {
        ws.usuario.PersonaController_Service service = new ws.usuario.PersonaController_Service();
        ws.usuario.PersonaController port = service.getPersonaControllerPort();
        return port.updateSaldo(code, nuevoSaldo);
    }

    private static String getPrecio(int code) {
        ws.ProductoController_Service service = new ws.ProductoController_Service();
        ws.ProductoController port = service.getProductoControllerPort();
        return port.getPrecio(code);
    }

