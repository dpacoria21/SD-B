    private void btnComprarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnComprarActionPerformed
        String cantidad = tfCantidad.getText();
        String codigo = tfCodigo.getText();

        if (cantidad.isEmpty() || codigo.isEmpty()) {
            tfResultado.setText("Los campos no pueden estar vacíos");
            return;
        }

        try {
            double saldo = Double.parseDouble(ISaldo.getText());
            int cant = Integer.parseInt(cantidad);
            int cod = Integer.parseInt(codigo);
            String ans = sellProduct(cod, cant, saldo);
            tfResultado.setText(ans);
            tAreaProductos.setText(getProducts());

            double newSaldo = saldo;
            if (saldo - cant * Double.parseDouble(getPrecio(cod)) >= 0) {
                newSaldo = saldo - cant * Double.parseDouble(getPrecio(cod));
            }
            updateSaldo(1, newSaldo);
            ISaldo.setText(newSaldo + "");

        } catch (NumberFormatException e) {

            tfResultado.setText("Los campos deben contener números válidos");
        }

    }//GEN-LAST:event_btnComprarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        String codigo = tfCodigo.getText();
        if (codigo.isEmpty()) {
            tfResultado.setText("Ingrese el codigo para realizar la busqueda");
        }
        try {
            int cod = Integer.parseInt(codigo);
            String ans = getProduct(cod);
            tAreaProductos.setText(ans);
        } catch (NumberFormatException e) {
            tfResultado.setText("El campo de codigo debe contener números validos");
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

